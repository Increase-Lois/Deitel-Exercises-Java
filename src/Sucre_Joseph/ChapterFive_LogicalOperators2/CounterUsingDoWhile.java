package Sucre_Joseph.ChapterFive_LogicalOperators2;
/*
  Counter-controlled iteration with the while iteration statement.
        */
public class CounterUsingDoWhile {
    public static void main(String[] args) {
        int counter = 1;
        do {
            System.out.printf("%d ", counter);
            counter++;
        }
        while (counter <= 10);
        System.out.println();
    }


        }


