package PraciceCodes;

public class PrintATable {
    public static void main(String[] args) {
        /*
        Write a program that displays the following table:
        a           a*2             a*3
        1           1                1
        2           4                8
        3           9               27
        4           16              64
         */

        //\t escape sequence for a tab space

        System.out.println("a\ta*2\t\ta*3");
        System.out.println("1\t1\t\t1");
        System.out.println("2\t4\t\t8");
        System.out.println("3\t9\t\t27");
        System.out.println("4\t16\t\t64");
    }


}
